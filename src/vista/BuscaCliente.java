    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        String convertir="";//variaable para convertir el combobox a columnas de la base de datos
        //INICIA EL CONVERSOR DE LOS CAMPOS PARA LA BD
        if(jCBBuscadorCliente.getSelectedIndex()==1){
            convertir = "nombre";
        }
        if(jCBBuscadorCliente.getSelectedIndex()==2){
            convertir = "contacto";
        }
        if(jCBBuscadorCliente.getSelectedIndex()==3){
            convertir = "tel";
        }
        if(jCBBuscadorCliente.getSelectedIndex()==4){
            convertir = "correo";
        }
        //FIN DE CONVERSOR

        if(jTxtBuscadorCliente.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo de texto vacío",
                "Aviso",JOptionPane.WARNING_MESSAGE);
            LimpiaTablaProveedores();
            cliente.TablaConsultaClientes();
            jTxtBuscadorCliente.requestFocusInWindow();
        }else{
            if (jCBBuscadorCliente.getSelectedIndex()==0) {
                JOptionPane.showMessageDialog(null, "Selecciona una opcicón de busqueda",
                    "Aviso",JOptionPane.WARNING_MESSAGE);
            }else{
                if(BD.conectarBD()){
                    try {
                        LimpiaTablaProveedores();
                        cliente.buscarCliente(convertir, jTxtBuscadorCliente.getText().trim());
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(rootPane, "No se consultó  correctamente la tabla de clientes: "+e,
                            "Aviso",JOptionPane.WARNING_MESSAGE);
                        BD.cerrarConexion();
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Error de conexión",
                        "Error",JOptionPane.ERROR_MESSAGE);
                    BD.cerrarConexion();
                }
            }
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

   public void LimpiaTablaProveedores(){
       //
        try {
            DefaultTableModel modelo = (DefaultTableModel)jTableClientes.getModel();
            int filas = jTableClientes.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
            System.out.println("Limpieza de tabla exitosa! en busqueda de proveedor");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al intentar limpiar la tabla clientes: "+e,
                    "Tabla Clientes",JOptionPane.ERROR_MESSAGE);
        }
    } 
